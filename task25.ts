let age : number = 18;

if( age <= 2){
    console.log("Person is a Baby");
}else if( age  <= 4){
    console.log("Person is a kid");
}else if( age <= 20){
    console.log("Person is a Teenager")
}else if( age  <= 65){
    console.log("Person is an Adult");
}else{
    console.log("Person is an Elder");
}
